/* ============================================================
 * Founders OS — setup-page generators (canonical, shared)
 * ============================================================
 * Pure, dependency-free, DOM-free generation functions for the setup
 * wizard: input object in, file string out. No `window`, no `fetch`, no
 * imports. This is the SINGLE SOURCE for the standalone setup page
 * (integrations/setup-page/index.html, which inlines this file at build
 * via build.js) AND the marketing-site setup page (synced from here by
 * tools/sync-setup-generators - never hand-edit the copy). Keeping the
 * generators in one place is what kills the drift between the two pages.
 *
 * Each page keeps its own DOM wiring and calls these for content. Loaded
 * as a classic script it attaches `FoundersOSSetup` to the global; under
 * Node (tests) it is available via module.exports. No ESM so the inlined
 * standalone page works opened straight from disk.
 *
 * Scheduler note: the generated OS units point at the tick WRAPPER
 * (integrations/scheduler/foundersos-tick.{sh,cmd}), not the bare CLI, so
 * one scheduled job runs `detect` then `run --hold-only` serially. The
 * wrapper reads credentials from the env file this module also generates
 * (default ~/.config/foundersos-tick.env), so the units carry no secrets.
 * ============================================================ */
(function (root) {
  "use strict";

  // ── Defaults (overridable via the opts object) ───────────
  var DEFAULTS = {
    label: "com.foundersos.tick",
    wrapperPathUnix: "~/.local/bin/foundersos-tick.sh",
    wrapperPathWin: "%USERPROFILE%\\foundersos-tick.cmd",
    envPath: "~/.config/foundersos-tick.env",
    logPath: "/tmp/foundersos-tick.log",
    cadence: "hourly", // "hourly" | "daily"
    dailyHour: 6, // used when cadence === "daily"
    taskName: "FoundersOS Tick",
  };

  function withDefaults(opts) {
    var o = {};
    for (var k in DEFAULTS) o[k] = DEFAULTS[k];
    if (opts) for (var j in opts) if (opts[j] != null && opts[j] !== "") o[j] = opts[j];
    return o;
  }

  // ── setup.sql: substitute the chosen embedding dimension ──
  // The raw setup.sql ships with vector(1024) as the sentinel; the page
  // replaces it with the provider's dimension. Pure string transform.
  function sqlForDimension(raw, dim) {
    return String(raw).replace(/vector\(1024\)/g, "vector(" + dim + ")");
  }

  // ── mcp.json ──────────────────────────────────────────────
  // env: the resolved environment object (built by the page from its DOM).
  // opts.useLatest pins @latest vs the bare package spec.
  function buildMcpJson(env, opts) {
    var useLatest = !!(opts && opts.useLatest);
    var pkg = "@ourthinktank/founders-os" + (useLatest ? "@latest" : "");
    return { mcpServers: { "founders-os": { command: "npx", args: ["-y", pkg], env: env || {} } } };
  }

  // ── env file (creds for the scheduled wrapper) ───────────
  // Scheduled jobs do not inherit your shell profile, so the wrapper loads
  // these from a file. Same env the MCP server uses, as KEY=value lines.
  function buildEnvFile(env) {
    var e = env || {};
    var lines = [
      "# Founders OS tick credentials — loaded by foundersos-tick.sh/.cmd.",
      "# Keep this file private (chmod 600). Generated by the setup page.",
    ];
    var order = [
      "SUPABASE_URL", "SUPABASE_SECRET_KEY",
      "FOUNDERS_OS_COMPANY_ID", "FOUNDERS_OS_USER_ID", "FOUNDERS_OS_TIMEZONE",
      "EMBEDDING_PROVIDER", "EMBEDDING_MODEL", "EMBEDDING_DIM",
      "OPENAI_API_KEY",
      "AWS_ACCESS_KEY_ID", "AWS_SECRET_ACCESS_KEY", "AWS_DEFAULT_REGION",
      "OLLAMA_BASE_URL",
    ];
    var seen = {};
    for (var i = 0; i < order.length; i++) {
      var k = order[i];
      seen[k] = true;
      if (e[k] != null && e[k] !== "") lines.push(k + "=" + e[k]);
    }
    // Any extra keys the page added that we did not enumerate.
    for (var key in e) {
      if (!seen[key] && e[key] != null && e[key] !== "") lines.push(key + "=" + e[key]);
    }
    return lines.join("\n") + "\n";
  }

  // ── Cadence helpers ──────────────────────────────────────
  function cronExpr(o) {
    return o.cadence === "daily" ? "0 " + o.dailyHour + " * * *" : "0 * * * *";
  }
  function systemdOnCalendar(o) {
    return o.cadence === "daily"
      ? "*-*-* " + pad2(o.dailyHour) + ":00:00"
      : "hourly";
  }
  function pad2(n) { return (n < 10 ? "0" : "") + n; }

  // ── launchd (macOS) — points at the wrapper ──────────────
  function buildLaunchdPlist(opts) {
    var o = withDefaults(opts);
    var interval = o.cadence === "daily"
      ? "<dict><key>Hour</key><integer>" + o.dailyHour + "</integer><key>Minute</key><integer>0</integer></dict>"
      : "<dict><key>Minute</key><integer>0</integer></dict>";
    return [
      '<?xml version="1.0" encoding="UTF-8"?>',
      '<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">',
      '<plist version="1.0">',
      "<dict>",
      "  <key>Label</key><string>" + o.label + "</string>",
      "  <key>ProgramArguments</key>",
      "  <array>",
      "    <string>" + o.wrapperPathUnix + "</string>",
      "  </array>",
      "  <key>StartCalendarInterval</key>" + interval,
      "  <key>StandardErrorPath</key><string>" + o.logPath + "</string>",
      "  <key>StandardOutPath</key><string>" + o.logPath + "</string>",
      "</dict>",
      "</plist>",
      "",
    ].join("\n");
  }

  // ── systemd (Linux) — service + timer, points at the wrapper ──
  function buildSystemdService(opts) {
    var o = withDefaults(opts);
    return [
      "[Unit]",
      "Description=Founders OS tick (detect + run --hold-only)",
      "",
      "[Service]",
      "Type=oneshot",
      "ExecStart=" + o.wrapperPathUnix,
      "",
    ].join("\n");
  }
  function buildSystemdTimer(opts) {
    var o = withDefaults(opts);
    return [
      "[Unit]",
      "Description=Schedule the Founders OS tick",
      "",
      "[Timer]",
      "OnCalendar=" + systemdOnCalendar(o),
      "Persistent=true",
      "",
      "[Install]",
      "WantedBy=timers.target",
      "",
    ].join("\n");
  }

  // ── cron (Linux/macOS) — one line at the wrapper ─────────
  function buildCronLine(opts) {
    var o = withDefaults(opts);
    return cronExpr(o) + " " + o.wrapperPathUnix + " >> " + o.logPath + " 2>&1\n";
  }

  // ── Task Scheduler (Windows) — registers the .cmd wrapper ──
  function buildTaskSchedulerCmd(opts) {
    var o = withDefaults(opts);
    var sched = o.cadence === "daily"
      ? "/SC DAILY /ST " + pad2(o.dailyHour) + ":00"
      : "/SC HOURLY";
    return [
      "REM Register the Founders OS tick with Windows Task Scheduler.",
      "REM Runs the wrapper .cmd, which sets creds and runs detect then run --hold-only.",
      'schtasks /Create /TN "' + o.taskName + '" /TR "' + o.wrapperPathWin + '" ' + sched + " /F",
      'schtasks /Query  /TN "' + o.taskName + '"',
      'schtasks /Run    /TN "' + o.taskName + '"',
      "",
    ].join("\n");
  }

  // ── Bundle: everything the page needs for the scheduler section ──
  function buildSchedulerBundle(env, opts) {
    return {
      envFile: buildEnvFile(env),
      launchd: buildLaunchdPlist(opts),
      systemdService: buildSystemdService(opts),
      systemdTimer: buildSystemdTimer(opts),
      cron: buildCronLine(opts),
      taskScheduler: buildTaskSchedulerCmd(opts),
    };
  }

  var FoundersOSSetup = {
    DEFAULTS: DEFAULTS,
    sqlForDimension: sqlForDimension,
    buildMcpJson: buildMcpJson,
    buildEnvFile: buildEnvFile,
    buildLaunchdPlist: buildLaunchdPlist,
    buildSystemdService: buildSystemdService,
    buildSystemdTimer: buildSystemdTimer,
    buildCronLine: buildCronLine,
    buildTaskSchedulerCmd: buildTaskSchedulerCmd,
    buildSchedulerBundle: buildSchedulerBundle,
  };

  if (typeof module !== "undefined" && module.exports) module.exports = FoundersOSSetup;
  else root.FoundersOSSetup = FoundersOSSetup;
})(typeof self !== "undefined" ? self : this);
